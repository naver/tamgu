	Tamgu* divide(Tamgu* a, bool itself) {
		double v = a->Float();
		if (v == 0)
			return globalTamgu->Returnerror("Error: Divided by 0");
		if (itself) {
			value /= v;
			return this;
		}

		v = value / v;
		return globalTamgu->ProvideConstfloat(v);
	}

	Tamgu* power(Tamgu* a, bool itself) {		
		if (itself) {
			value = pow(value, a->Float());
			return this;
		}

		double v = pow(value, a->Float());
		return globalTamgu->ProvideConstfloat(v);
	}

	Tamgu* shiftleft(Tamgu* a, bool itself) {
        double64 d(value);
        d.bits <<= a->Long();
        
        if (itself) {
            value = d.v;
            return this;
        }
        
        return globalTamgu->ProvideConstfloat(d.v);
	}

	Tamgu* shiftright(Tamgu* a, bool itself) {
        double64 d(value);
        d.bits >>= a->Long();
        
        if (itself) {
            value = d.v;
            return this;
        }
        
        return globalTamgu->ProvideConstfloat(d.v);
	}

	Tamgu* mod(Tamgu* a, bool itself) {
		long v = a->Integer();
		if (v == 0)
			return globalTamgu->Returnerror("Error: Divided by 0");
		if (itself) {
			value = (long)value % v;
			return this;
		}

		v = (long)value % v;
		return globalTamgu->ProvideConstfloat(v);
	}

